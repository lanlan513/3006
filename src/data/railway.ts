export const getStationById = (id: string): RailwayStation | undefined => {
  return RAILWAY_STATIONS.find(s => s.id === id);
};

export const getRouteById = (id: string): RailwayRoute | undefined => {
  return RAILWAY_ROUTES.find(r => r.id === id);
};

export const getTrainById = (id: string): MagicTrain | undefined => {
  return INITIAL_TRAINS.find(t => t.id === id);
};

export const getEventById = (id: string): RailwayEvent | undefined => {
  return RAILWAY_EVENTS.find(e => e.id === id);
};

export const getRoutesByStation = (stationId: string): RailwayRoute[] => {
  return RAILWAY_ROUTES.filter(r => 
    r.startStationId === stationId || r.endStationId === stationId
  );
};

export const getStationsByRegion = (region: string): RailwayStation[] => {
  return RAILWAY_STATIONS.filter(s => s.region === region);
};

export const getBuiltStations = (builtIds: Set<string>): RailwayStation[] => {
  return RAILWAY_STATIONS.filter(s => builtIds.has(s.id) || s.built);
};

export const getBuiltRoutes = (builtIds: Set<string>): RailwayRoute[] => {
  return RAILWAY_ROUTES.filter(r => builtIds.has(r.id) || r.built);
};

export const canBuildStation = (stationId: string, magicCrystals: number, builtStations: Set<string>): boolean => {
  const station = getStationById(stationId);
  if (!station) return false;
  if (builtStations.has(stationId) || station.built) return false;
  if (magicCrystals < station.buildCost) return false;
  return true;
};

export const canBuildRoute = (routeId: string, magicCrystals: number, builtRoutes: Set<string>, builtStations: Set<string>): boolean => {
  const route = getRouteById(routeId);
  if (!route) return false;
  if (builtRoutes.has(routeId) || route.built) return false;
  if (magicCrystals < route.buildCost) return false;
  
  const startStation = getStationById(route.startStationId);
  const endStation = getStationById(route.endStationId);
  if (!startStation || !endStation) return false;
  
  const startBuilt = builtStations.has(route.startStationId) || startStation.built;
  const endBuilt = builtStations.has(route.endStationId) || endStation.built;
  
  return startBuilt && endBuilt;
};
