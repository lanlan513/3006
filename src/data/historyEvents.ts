export function getHistoricalEventById(id: string): HistoricalEvent | undefined {
  return historicalEvents.find((e) => e.id === id);
}

export function getHistoricalFigureById(id: string): HistoricalFigure | undefined {
  return historicalFigures.find((f) => f.id === id);
}

export function getEventsByEra(era: string): HistoricalEvent[] {
  return historicalEvents.filter((e) => e.era === era);
}

export function getEventsByRegion(region: string): HistoricalEvent[] {
  if (region === '全部') return historicalEvents;
  return historicalEvents.filter((e) => e.region === region);
}

export function getEventsByType(type: string): HistoricalEvent[] {
  if (type === '全部') return historicalEvents;
  return historicalEvents.filter((e) => e.type === type);
}

export function searchHistoricalEvents(query: string): HistoricalEvent[] {
  const lowerQuery = query.toLowerCase();
  return historicalEvents.filter(
    (e) =>
      e.title.toLowerCase().includes(lowerQuery) ||
      e.description.toLowerCase().includes(lowerQuery) ||
      e.location.toLowerCase().includes(lowerQuery) ||
      e.participants.some((p) => p.name.toLowerCase().includes(lowerQuery)) ||
      e.consequences.some((c) => c.toLowerCase().includes(lowerQuery))
  );
}

export function searchHistoricalFigures(query: string): HistoricalFigure[] {
  const lowerQuery = query.toLowerCase();
  return historicalFigures.filter(
    (f) =>
      f.name.toLowerCase().includes(lowerQuery) ||
      f.role.toLowerCase().includes(lowerQuery) ||
      f.description.toLowerCase().includes(lowerQuery) ||
      f.achievements.some((a) => a.toLowerCase().includes(lowerQuery))
  );
}
